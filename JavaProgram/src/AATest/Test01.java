package AATest;
import java.util.Scanner;
/*
郑轻OJ 1000：整数a+b
 */
public class Test01 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int x, y;
        while(sc.hasNext()) {
            x = sc.nextInt();
            y = sc.nextInt();
            System.out.println(x + y);
        }
    }
}
